let num_A = 10;
let num_B = 5;

let resultado_adicao = num_A + num_B;
console.log(`Resultado da adição entre ${num_A} e ${num_B} é ${resultado_adicao}`);

let resultado_subtracao = num_A - num_B;
console.log(`Resultado da subtração entre ${num_A} e ${num_B} é ${resultado_subtracao}`);

let resultado_multiplicacao = num_A * num_B;
console.log(`Resultado da multiplicação entre ${num_A} e ${num_B} é ${resultado_multiplicacao}`);

let resultado_divisao = num_A / num_B;
console.log(`Resultado da divisão entre ${num_A} e ${num_B} é ${resultado_divisao}`);
